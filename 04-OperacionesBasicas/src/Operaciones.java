public class Operaciones {
	/**
	 * 
	 * @param n1 Valor de la Primera caja de texto
	 * @param n2 Valor de la Segunda Caja de Texto
	 * @return Regresa el valor de la suma en formato Double
	 */

	public double sumar(double n1, double n2){
		return n1+n2;
		
	}
	/**
	 * 
	 * @param n1 Valor de la Primera caja de texto
	 * @param n2 Valor de la Segunda Caja de Texto
	 * @return Regresa el valor de la resta en formato Double
	 */
	public double restar(double n1, double n2){
		return n1-n2;
		
	}
	/**
	 * 
	 * @param n1 Valor de la Primera caja de texto
	 * @param n2 Valor de la Segunda Caja de Texto
	 * @return Regresa el valor de la multiplicación en formato Double
	 */
	public double multiplicar(double n1, double n2){
		return n1*n2;
	}
	/**
	 * 
	 * @param n1 Valor de la Primera caja de texto
	 * @param n2 Valor de la Segunda Caja de Texto
	 * @return Regresa el valor de la división en formato Double
	 */
	public double dividir(double n1, double n2){
		return n1/n2;
	}
}
